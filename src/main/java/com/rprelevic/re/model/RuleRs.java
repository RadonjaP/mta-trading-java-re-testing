package com.rprelevic.re.model;

public class RuleRs {

    private final String info;

    private final boolean result;

    public RuleRs(String info, boolean result) {
        this.info = info;
        this.result = result;
    }

    public String getInfo() {
        return info;
    }

    public boolean getResult() {
        return result;
    }

}